#6

# Declare product prices
coffee = 2.50
cappuccino = 3.75
espresso = 4.00

# Calculate the subtotal for the order
# 3 coffees, 4 cappuccinos, and 2 espressos
subtotal = (3 * coffee) + (4 * cappuccino) + (2 * espresso)

# Define sales tax rate
SALES_TAX = 0.08  # 8%

# Calculate total sale with sales tax
total_sale = subtotal + (subtotal * SALES_TAX)

# Print subtotal and total sale
print(f"Subtotal: ${subtotal:.2f}")
print(f"Total Sale (including sales tax): ${total_sale:.2f}")


